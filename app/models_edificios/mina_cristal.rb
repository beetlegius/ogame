# Las minas de cristal producen cristal.
class MinaCristal < Mina

  ##############################################################################
  #### CONFIGURACIONES Y RELACIONES
  ##############################################################################

  establecer_costos metal: 48, cristal: 24
  factor 1.6

  ##############################################################################
  #### SCOPES Y VALIDACIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PÚBLICOS
  ##############################################################################

  def produccion_por_hora
    (20 * nivel * 1.1**nivel).round
  end

  def consumo_energia_base
    10 * nivel * 1.1**nivel
  end

  ##############################################################################
  #### ALIAS E IMPRESIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PRIVADOS
  ##############################################################################

  private

end
