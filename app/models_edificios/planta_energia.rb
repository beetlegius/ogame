# Las plantas de energía producen energía para mantener las minas funcionando.
class PlantaEnergia < Edificio

  ##############################################################################
  #### CONFIGURACIONES Y RELACIONES
  ##############################################################################

  establecer_costos metal: 75, cristal: 30
  factor 1.5

  ##############################################################################
  #### SCOPES Y VALIDACIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PÚBLICOS
  ##############################################################################

  def produccion
    (produccion_general * porcentaje_produccion / 100).round
  end

  def produccion_general
    20 * nivel * 1.1**nivel
  end

  ##############################################################################
  #### ALIAS E IMPRESIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PRIVADOS
  ##############################################################################

  private

end
