# El hangar se utiliza para construir naves y defensas.
class Hangar < Edificio

  ##############################################################################
  #### CONFIGURACIONES Y RELACIONES
  ##############################################################################

  establecer_costos metal: 400, cristal: 200, deuterio: 100

  ##############################################################################
  #### SCOPES Y VALIDACIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PÚBLICOS
  ##############################################################################

  def puede_expandirse?
    super && propietario.naves_ataque.fabricando.empty? && propietario.naves_defensa.fabricando.empty?
  end

  def cumple_requisitos?
    propietario.fabrica_robots.nivel >= 2 && super
  end

  ##############################################################################
  #### ALIAS E IMPRESIONES
  ##############################################################################


  ##############################################################################
  #### MÉTODOS PRIVADOS
  ##############################################################################

  private

end
